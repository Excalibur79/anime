 var x=1
   $("#kimi-button").click(function()
   {
       if(($("#kimi-button").html())=="Read More")
       {
            $("#dotskimi").css("display","none");
            $("#morekimi").css("display","block");

       }
       if(($("#kimi-button").html())=="Read Less")
       {
            $("#dotskimi").css("display","block");
            $("#morekimi").css("display","none");

       }
         if(x%2!=0)
            {
                 $("#kimi-button").html("Read Less");
                 x=x+1;
            }
           else
           {
                 $("#kimi-button").html("Read More");
                 x=x+1;
           }


   });
